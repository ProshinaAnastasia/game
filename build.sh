#!/bin/bash

# Создание директории для сборки, если она не существует
mkdir -p build

# Переход в директорию для сборки
cd build || exit 1

# Запуск CMake для генерации файлов сборки
echo "Конфигурирование проекта с помощью CMake..."
cmake .. || { echo "Ошибка конфигурирования CMake"; exit 1; }

# Компиляция проекта
echo "Сборка проекта..."
make || { echo "Ошибка сборки"; exit 1; }

echo "Сборка успешно завершена!"
echo "Запустите игру командой: ./snake"

# Сделать исполняемый файл
chmod +x snake
